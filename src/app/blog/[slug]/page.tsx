interface Props { params: Promise<{ slug: string }> }

export async function generateStaticParams() {
  return Object.keys(posts).map((slug) => ({ slug }));
}

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { slug } = await params;
  const post = posts[slug];
  if (!post) return {};
  return {
    title: `${post.title} | PergoClean Blog`,
    description: post.excerpt,
    openGraph: { title: post.title, description: post.excerpt, type: "article", publishedTime: post.date },
  };
}

function blogPostingJsonLd(post: typeof posts[keyof typeof posts], slug: string) {
  return {
    "@context": "https://schema.org",
    "@type": "BlogPosting",
    headline: post.title,
    description: post.excerpt,
    datePublished: post.date,
    author: { "@type": "Person", name: post.author },
    publisher: { "@type": "Organization", name: "PergoClean", logo: { "@type": "ImageObject", url: "https://www.pergoclean.com.tr/logo.webp" } },
    url: `https://www.pergoclean.com.tr/blog/${slug}`,
    image: "https://www.pergoclean.com.tr/logo.webp",
  };
}

export default async function BlogPostPage({ params }: Props) {
  const { slug } = await params;
  const post = posts[slug];
  if (!post) notFound();

  const jsonLd = blogPostingJsonLd(post, slug);

  return (
    <article className="container mx-auto px-4 py-20">
      <script type="application/ld+json" dangerouslySetInnerHTML={{ __html: JSON.stringify(jsonLd) }} />
      <div className="mx-auto max-w-3xl">
        <div className="text-xs font-semibold uppercase tracking-[0.28em] text-[var(--aqua)]">Blog</div>
        <h1 className="mt-3 text-balance text-4xl font-bold md:text-5xl">{post.title}</h1>
        <div className="mt-4 flex items-center gap-4 text-sm text-muted-foreground">
          <span>{post.date}</span>
          <span>·</span>
          <span>{post.author}</span>
        </div>
        <div className="mt-8 aspect-[16/9] relative overflow-hidden rounded-3xl">
          <Image src={post.image} alt={post.title} fill className="object-cover" />
        </div>
        <div className="mt-10 prose prose-pergoclean" dangerouslySetInnerHTML={{ __html: post.content.replace(/\n/g, "<br/>").replace(/## /g, "<h2>").replace(/### /g, "<h3>").replace(/<\/h2>/g, "</h2>").replace(/<\/h3>/g, "</h3>") }} />
        <div className="mt-12 text-center">
          <a href="/#fiyat" className="inline-flex items-center gap-2 rounded-full bg-aqua-grad px-8 py-4 text-sm font-semibold text-white shadow-glow transition hover:scale-105">
            Profesyonel Destek Al
          </a>
        </div>
      </div>
    </article>
  );
}
